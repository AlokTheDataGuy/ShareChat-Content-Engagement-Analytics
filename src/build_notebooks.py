    "\n"
    "DB_PATH = pathlib.Path('../data/warehouse/sharechat_warehouse.db')\n"
    "def connect():\n"
    "    conn = sqlite3.connect(DB_PATH)\n"
    "    conn.create_function('SQRT',  1, lambda x: math.sqrt(max(x,0)) if x is not None else None)\n"
