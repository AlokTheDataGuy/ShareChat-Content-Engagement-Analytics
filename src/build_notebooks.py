    "n_act = int(sample['users'].min())\n"
    "print(f'\\nRequired sample (80% power, alpha=0.05): {n_req:,.0f}')\n"
    "print(f'Actual sample per group:                  {n_act:,.0f}')\n"
    "print(f'Study is {\"ADEQUATELY\" if n_act >= n_req else \"UNDER\"} powered')"
