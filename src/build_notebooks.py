    "diff = var.mean() - ctrl.mean()\n"
    "ci95_lo = diff - 1.96*se\n"
    "ci95_hi = diff + 1.96*se\n"
    "\n"
    "print(f'Control  n={len(ctrl):,}  mean={ctrl.mean():.1f}s  std={ctrl.std():.1f}s')\n"
